#!/bin/bash

# проверяем сколько передано аргументов
if [ $# -ne 1 ]; then
    echo "Некорректный запрос"
    exit 1
fi

# проверяем, что число положительное целое
if ! [[ $1 =~ ^[1-9][0-9]*$ ]]; then
    echo "Аргумент должен быть положительным целым числом"
    exit 1
fi

# находим середину
n=$1
half=$((n / 2))

# произведение первой половины
mult=1
for ((i=1; i<=half; i++)); do
    mult=$((mult * i))
done

# сумма второй половины
sum=0
start=$((half + 1))
if [ $((n % 2)) -eq 1 ]; then
    # для нечетного числа пропускаем середину
    start=$((half + 2))
fi

for ((i=start; i<=n; i++)); do
    sum=$((sum + i))
done

# вывод результатов
echo "mult: $mult"
echo "sum: $sum"